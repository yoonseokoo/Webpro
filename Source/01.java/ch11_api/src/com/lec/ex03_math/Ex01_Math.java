package com.lec.ex03_math;

public class Ex01_Math {
	public static void main(String[] args) {
		int a = 2, b = 10;
		System.out.println("2 to the power of 10 ? " + Math.pow(a, b));
		System.out.println("absolute value of -9.9 ? " + Math.abs(-9.9));
		System.out.println("square root of 4 ? " + Math.sqrt(4));
		System.out.println("smaller value between a & b ? " + Math.min(a, b));
		System.out.println("larger value between a & b ? " + Math.max(a, b));
		// Math variables (static final variables)
		System.out.println("PI is ? " + Math.PI);
	}
}
