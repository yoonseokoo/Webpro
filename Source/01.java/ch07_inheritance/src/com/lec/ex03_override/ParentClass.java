package com.lec.ex03_override;

public class ParentClass {
	public ParentClass() {
		System.out.println("ParentClass constructor");
	}

	public ParentClass(int i) {
		System.out.println("ParentClass constructor with 1 parameter\n");
	}

	public void method1() {
		System.out.println("ParentClass method1() function\n");
	}

	public void method2() {
		System.out.println("ParentClass method2() function\n");
	}
}
